def main():
    client = create_client(SUPABASE_URL, SUPABASE_KEY)

    updated = 0
    errors = 0

    for product_id, aliases in ALIASES_MAP.items():
        try:
            result = client.table(PRODUCT_TABLE).update(
                {'aliases': aliases}
            ).eq('id', product_id).execute()

            if result.data:
                name = result.data[0].get('product_name', '?')
                print(f"  OK  id={product_id:3d} | {name} | {aliases[:60]}")
                updated += 1
            else:
                print(f"  SKIP id={product_id:3d} | no matching row")
        except Exception as e:
            print(f"  ERR id={product_id:3d} | {e}")
            errors += 1

    print(f"\nDone: {updated} updated, {errors} errors, {len(ALIASES_MAP)} total")


if __name__ == '__main__':
    main()
